extern int g_Table8PSK2[];
extern int g_expQ15_table2[];

#define PHI_DIV  32767
#define PHI_EIGHT 4095
#define FACTOR_1 2317  //   1/sqrt(length) Q15
#define FACTOR_2 3276  //   sqrt(2/length)
#define SOUND_NUM 2
#define HOLD_TIME 9

