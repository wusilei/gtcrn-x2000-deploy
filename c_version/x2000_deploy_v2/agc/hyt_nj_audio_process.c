void VAD_autocorrelation(short *data_in, 
                        unsigned short thr_level, 
                        unsigned int first_flag, 
                        short *voice_state, 
                        short *noise_state)
{

    int i,k,k2;
    short DiffPosi2_max;
    short Period_sum;
    short Posi_count;
    short data_temp;
    long long sum_zixiangguan;
    static short index = 0;
    static short mic_off_count = 0;

    *noise_state = 0;

    // łőĘĽ»Ż
    if(first_flag == 0)
    {
        index = 0;
        mic_off_count = 0;
        memset(Period, 0, sizeof(Period));
    }
    
    if (thr_level == 0)
    {
        *voice_state = 1;
    }
    else
    {
        //Çó×ÔĎŕąŘ
        for(k=0; k<200; k++)
        {
            sum_zixiangguan = 0;
            for(k2=0; k2<(200-k); k2++)
            {
                sum_zixiangguan = sum_zixiangguan + (long long)(data_in[k2]) * (long long)(data_in[k2 + k]);
            }
            
            AotoCorreSect[k] = (sum_zixiangguan>0) ? 1 : -1;
        }

        //ĹĐ¶ĎąýÁăµă¸öĘý
        Posi_count = 0;
        for(i=1; i<100; i++)
        {
            if(AotoCorreSect[i] * AotoCorreSect[i-1] < 0)
            {
                Posi_count++;
                Posi[Posi_count-1] = i;
            }
        }
        if (Posi_count== 0 || Posi_count== 1 || Posi_count== 2 || Posi_count>90)
        {
            Period[index] = 0;
        }   
        else
        {
            //ĹĐ¶Ď×ÔĎŕąŘÖµąýÁăµăĘÇ·ńľßÓĐÖÜĆÚĐÔ
            for(i=0;i<Posi_count-1;i++)
            {
                DiffPosi1[i]=Posi[i+1]-Posi[i];
            }

            for(i=0;i<Posi_count-2;i++)
            {
                DiffPosi2[i]=DiffPosi1[i+1]-DiffPosi1[i];
            }

            DiffPosi2_max = 0;
            for(i=0;i<Posi_count-2;i++)
            {
                data_temp = (DiffPosi2[i]>0)? DiffPosi2[i] : -DiffPosi2[i];
                DiffPosi2_max = (DiffPosi2_max>data_temp)? DiffPosi2_max : data_temp;
            }

            Period[index] = (DiffPosi2_max < SW_jingzao[thr_level-1][0])? 1 : 0;
        }

        index++;
        Period_sum = 0;
        //Á¬Đřindex_length°üĹĐ¶ĎŇ»´Î
        if(index == INDEX_LEN)
        {
            for(i=0; i<INDEX_LEN; i++)
            {
                Period_sum = Period_sum + Period[i];
            }

            if(Period_sum >= 6)
            {
                *voice_state = 1;
                mic_off_count = 0;
            }
            else
            {
                mic_off_count++;
                if(mic_off_count == 60)
                {
                    mic_off_count = 0;
                    *voice_state = 0;
                }
                *noise_state = 1;
            }
            
            index = INDEX_LEN - 1;
            memcpy(&Period[0],&Period[1],7*2);
        }
    }
}



