/*******************************************************************************************
Description     :   ÓďŇô»î¶ŻĽě˛â(VAD)Ł¬Ć×ěŘ·¨			      
	
Input           : 	dataInŁ¬   ĘäČëĘýľÝ
                    lengthŁ¬   ĘäČëĘýľÝł¤¶Č
					levelŁ¬    ĂĹĎŢÖµŁ¬µÚ0¸öÔŞËŘµÄ×îĐˇÖµŁ¬µÚ1¸öÔŞËŘĘÇ×î´óÖµ
                    vad_paraŁ¬ ˛ÎĘý˝áąąĚĺ
                    
Output          :   ÎŢ
			
Return          :  Ć×ěŘÖµ
Date            :  2019.1.14
Author          :  oufeifei        
********************************************************************************************/
int VAD_entropy(    short * dataIn, 
    		        unsigned int length,
    		        unsigned int * level, 
    		        VADParaStr * vad_para)
{
	unsigned int i, j;
	short temp1;
	int temp;
	int index, rotate, idx, val;
	int ck, entropy;
	int energy[200];
	long long  energy_sum=0;
	
	for(i=0; i<length; i++)
	{
		temp = 0;
		for(j=0; j<length; j++)
		{
			index = i * (2 * j+1);
			index = (index * 8192/length) & PHI_DIV;
			rotate = g_Table8PSK2[index>>12];
			idx = g_expQ15_table2[index & PHI_EIGHT];
			val = _cmpyr1(rotate, idx);

			temp1 = _cmpyr1((int)dataIn[j]<<16, val)>>16;
			temp = temp + temp1;
		}
		
		temp = (i<1) ? ((long long)temp * FACTOR_1>>10) : ((long long)temp * FACTOR_2>>10);//Q20
		energy[i] = _abs(temp);
		energy_sum += energy[i];//q20
	}

	entropy = 0;
	for(i=0; i<length; i++)
	{
		ck = ((long long)energy[i]<<20)/energy_sum;
	      entropy = entropy - ck * (log2((double)ck) - 20);
	}

	if(entropy < level[0])// ˝řČëÓďŇô¶Î
	{
		vad_para->gVoice_cnt += 1;
		vad_para->gSilent_cnt = 0;
		if(vad_para->gFlag_vox != 2)
		{
			vad_para->gFlag_vox = (vad_para->gVoice_cnt > SOUND_NUM) ? 2 : vad_para->gFlag_vox;
			vad_para->gFlag_vox = (vad_para->gFlag_vox == 0) ? 1 : vad_para->gFlag_vox;
		}
	}
	else if(entropy < level[1])// żÉÄÜ´¦ÓÚÓďŇô¶Î
	{
		vad_para->gVoice_cnt += 1;
		if(vad_para->gFlag_vox != 2)
		{
			if(((vad_para->gVoice_cnt - vad_para->gSilent_cnt)>HOLD_TIME) && (vad_para->gFlag_vox == 1))
			{
				vad_para->gFlag_vox = 2;
				vad_para->gSilent_cnt = 0;
			}
			vad_para->gSilent_cnt += 1;
		}
	}
	else  // ľ˛Ňô¶Î
	{
		vad_para->gVoice_cnt = 0;
		if(vad_para->gFlag_vox == 2)
		{
			vad_para->gSilent_cnt += 1;
			if(vad_para->gSilent_cnt > HOLD_TIME)
			{
				vad_para->gFlag_vox = 0;
				vad_para->gSilent_cnt = 0;
			}
		}
	}
	
	return entropy;
}




